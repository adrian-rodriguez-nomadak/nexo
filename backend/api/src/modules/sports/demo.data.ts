export function demoContext(id: string): AnalysisInput {
  const selected = demoMatches.find((item) => item.id === id) ?? demoMatches[0];
  return {
    match: selected,
    availability: availability[selected.id] ?? [],
    headToHead: h2h[selected.id] ?? [],
    restDays: { home: 6, away: 6 },
    coverage: {
      standings: true,
      form: true,
      availability: true,
      headToHead: (h2h[selected.id] ?? []).length > 0,
      weather: Boolean(selected.weather),
    },
  };
}
