function match(
  id: string,
  homeKey: string,
  awayKey: string,
  startsAt: string,
  matchday: string,
): MatchSummary {
  const home = teams[homeKey];
  const away = teams[awayKey];
  return {
    id,
    startsAt,
    matchday,
    venue: home.stadium ?? "Por confirmar",
    status: "scheduled",
    home: { ...home },
    away: { ...away },
    sources: ["demo"],
    dataFreshness: "Datos demostrativos",
  };
}

